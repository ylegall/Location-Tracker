	/**
	 * creates a random location within the
	 * continental United States.
	 * @return
	 */
	public Location getRandomLocation() {
		Location l = new Location("DummyProvider");
		l.setTime(System.currentTimeMillis());
		l.setLatitude(25 + random.nextDouble() * 25);
		l.setLongitude(-70 - random.nextDouble() * 55);
//		l.setSpeed(1); // meters per second
//		l.setAccuracy(0); // accuracy in meters
		return l;
	}
	
